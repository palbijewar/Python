# 1. Age Group Categorization

# Classify a person's age group: Child (< 13), Teenager (13-19), Adult (20-59), Senior (60+).

user_age = 25

if user_age < 13:
    print("User is a child")
elif user_age < 20:
    print("User is a teen")
elif user_age < 59:
    print("User is an adult")
else:
    print("User is a senior")

